# Q. Take an integer input from the user and check whether the number is:


# Taking a number input from useer.
num = int(input("Enter a number : "))


# Check whether the number is Positive, Negative and Zero using conditions statement concepts.
if num > 0:
    print(f"The number {num} is Positive number.")
elif num < 0:
    print(f"The number {num} is Negative number.")
else:
    print(f"The number {num} is Zero.")